// Sibling class
package as9;

public class Sibling {
	private String name;
	private int age;
	private int weigth;
	
	// Main Constructor!
	public Sibling(String name, int age, int weigth) {
		super();
		this.name = name;
		this.age = age;
		this.weigth = weigth;
	}
		
	public String getName() {
		return name;
	}
		
	public int getAge() {
		return age;
	}
		
	public int getWeigth() {
		return weigth;
	}
	
}
